public class SwitchCase {
    public static void main(String[] args) {
        String  fruits = "Apple";
       switch(fruits){

        case "Apple":
        System.out.println("Red fruits");
        break;

        case "Banana":
        System.out.println("Yello fruits");
        break;

        default:
         System.out.println("unknown fruits");
       }
    }
}
